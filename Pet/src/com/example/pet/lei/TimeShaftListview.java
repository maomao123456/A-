package com.example.pet.lei;

import java.util.List;

public class TimeShaftListview {
	int head;
	String time;
	String word;
	List<SquareGridview> list;
	
	public int getHead() {
		return head;
	}
	public void setHead(int head) {
		this.head = head;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public List<SquareGridview> getList() {
		return list;
	}
	public void setList(List<SquareGridview> list) {
		this.list = list;
	}
	
}
